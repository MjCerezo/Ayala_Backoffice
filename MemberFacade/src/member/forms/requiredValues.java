package member.forms;

public class requiredValues {
	private Boolean region = false;
	private Boolean stateprovince = false;
	private Boolean city = false;
	private Boolean postal = false;
	public Boolean getRegion() {
		return region;
	}
	public void setRegion(Boolean region) {
		this.region = region;
	}
	public Boolean getStateprovince() {
		return stateprovince;
	}
	public void setStateprovince(Boolean stateprovince) {
		this.stateprovince = stateprovince;
	}
	public Boolean getCity() {
		return city;
	}
	public void setCity(Boolean city) {
		this.city = city;
	}
	public Boolean getPostal() {
		return postal;
	}
	public void setPostal(Boolean postal) {
		this.postal = postal;
	}
}
