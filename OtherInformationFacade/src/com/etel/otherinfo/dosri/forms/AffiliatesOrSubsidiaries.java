package com.etel.otherinfo.dosri.forms;

public class AffiliatesOrSubsidiaries {
	private String name;
	private String position;
	private String cifno;
	private String company;
	private String indivcifno;
	private String subAffCompany;
	
	
	public String getSubAffCompany() {
		return subAffCompany;
	}
	public void setSubAffCompany(String subAffCompany) {
		this.subAffCompany = subAffCompany;
	}
	public String getIndivcifno() {
		return indivcifno;
	}
	public void setIndivcifno(String indivcifno) {
		this.indivcifno = indivcifno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getCifno() {
		return cifno;
	}
	public void setCifno(String cifno) {
		this.cifno = cifno;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
}
