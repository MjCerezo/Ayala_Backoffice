package com.etel.otherinfo.pep.forms;

public class PEPViewForm {
	private String cifno;
	private String name;
	private String relationship;
	private boolean q1;
	private boolean q2;
	private boolean q3;
	private String pepstatus;
	public String getCifno() {
		return cifno;
	}
	public void setCifno(String cifno) {
		this.cifno = cifno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRelationship() {
		return relationship;
	}
	public void setRelationship(String relationship) {
		this.relationship = relationship;
	}
	public boolean isQ1() {
		return q1;
	}
	public void setQ1(boolean q1) {
		this.q1 = q1;
	}
	public boolean isQ2() {
		return q2;
	}
	public void setQ2(boolean q2) {
		this.q2 = q2;
	}
	public boolean isQ3() {
		return q3;
	}
	public void setQ3(boolean q3) {
		this.q3 = q3;
	}
	public String getPepstatus() {
		return pepstatus;
	}
	public void setPepstatus(String pepstatus) {
		this.pepstatus = pepstatus;
	}
	
	
}
