package com.etel.inquiry.forms;

public class NonMemberInquiryForm {
	
	private String name;
	private String type;
	private String codeno;
	private String refMemberName;
	private String referencetype;
	private String acctno;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCodeno() {
		return codeno;
	}
	public void setCodeno(String codeno) {
		this.codeno = codeno;
	}
	public String getRefMemberName() {
		return refMemberName;
	}
	public void setRefMemberName(String refMemberName) {
		this.refMemberName = refMemberName;
	}
	public String getReferencetype() {
		return referencetype;
	}
	public void setReferencetype(String referencetype) {
		this.referencetype = referencetype;
	}
	public String getAcctno() {
		return acctno;
	}
	public void setAcctno(String acctno) {
		this.acctno = acctno;
	}
	
	
	

}
