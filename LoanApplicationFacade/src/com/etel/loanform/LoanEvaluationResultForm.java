package com.etel.loanform;

public class LoanEvaluationResultForm {
	
	private String rank;
	private String tenure;
	private String position;
	private String maxLoanAmount;
	private String nthp;
	private String maxNoLoan;
	
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getTenure() {
		return tenure;
	}
	public void setTenure(String tenure) {
		this.tenure = tenure;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getMaxLoanAmount() {
		return maxLoanAmount;
	}
	public void setMaxLoanAmount(String maxLoanAmount) {
		this.maxLoanAmount = maxLoanAmount;
	}
	public String getNthp() {
		return nthp;
	}
	public void setNthp(String nthp) {
		this.nthp = nthp;
	}
	public String getMaxNoLoan() {
		return maxNoLoan;
	}
	public void setMaxNoLoan(String maxNoLoan) {
		this.maxNoLoan = maxNoLoan;
	}
	
	

}
