package com.etel.reports.forms;

public class AccountOfficersForm {
	
	private String lastname;
	private String firstname;
	private String middlename;
	private String suffix;
	private String accountofficers;
	private String aocode;
	
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getMiddlename() {
		return middlename;
	}
	public void setMiddlename(String middlename) {
		this.middlename = middlename;
	}
	public String getSuffix() {
		return suffix;
	}
	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}
	public String getAccountofficers() {
		return accountofficers;
	}
	public void setAccountofficers(String accountofficers) {
		this.accountofficers = accountofficers;
	}
	public String getAocode() {
		return aocode;
	}
	public void setAocode(String aocode) {
		this.aocode = aocode;
	}
}
