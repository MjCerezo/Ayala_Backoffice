package com.etel.evaluation.forms;

public class CoMakerForm {
	
	private String appno;
	private String cifno;
	private String customername;
	private String participationcode;
	private String fulladdress1;
	
	public String getAppno() {
		return appno;
	}
	public void setAppno(String appno) {
		this.appno = appno;
	}
	public String getCifno() {
		return cifno;
	}
	public void setCifno(String cifno) {
		this.cifno = cifno;
	}
	public String getCustomername() {
		return customername;
	}
	public void setCustomername(String customername) {
		this.customername = customername;
	}
	public String getParticipationcode() {
		return participationcode;
	}
	public void setParticipationcode(String participationcode) {
		this.participationcode = participationcode;
	}
	public String getFulladdress1() {
		return fulladdress1;
	}
	public void setFulladdress1(String fulladdress1) {
		this.fulladdress1 = fulladdress1;
	}
	
}
