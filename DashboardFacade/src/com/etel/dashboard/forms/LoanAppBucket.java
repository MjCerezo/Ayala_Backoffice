package com.etel.dashboard.forms;

public class LoanAppBucket {
	private Integer booked = 0;
	private Integer declined = 0;
	private Integer rejected = 0;
	private Integer foramendment = 0;
	public Integer getBooked() {
		return booked;
	}
	public void setBooked(Integer booked) {
		this.booked = booked;
	}
	public Integer getDeclined() {
		return declined;
	}
	public void setDeclined(Integer declined) {
		this.declined = declined;
	}
	public Integer getRejected() {
		return rejected;
	}
	public void setRejected(Integer rejected) {
		this.rejected = rejected;
	}
	public Integer getForamendment() {
		return foramendment;
	}
	public void setForamendment(Integer foramendment) {
		this.foramendment = foramendment;
	}
	

}
