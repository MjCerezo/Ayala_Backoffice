package com.etel.dashboard.forms;

public class MyMembership {
	private Integer newmemberapp = 0;
	private Integer resignation = 0;
	private Integer retention = 0;
	private Integer updateprofile = 0;
	
	public Integer getNewmemberapp() {
		return newmemberapp;
	}
	public Integer getResignation() {
		return resignation;
	}
	public Integer getRetention() {
		return retention;
	}
	public void setNewmemberapp(Integer newmemberapp) {
		this.newmemberapp = newmemberapp;
	}
	public void setResignation(Integer resignation) {
		this.resignation = resignation;
	}
	public void setRetention(Integer retention) {
		this.retention = retention;
	}
	public Integer getUpdateprofile() {
		return updateprofile;
	}
	public void setUpdateprofile(Integer updateprofile) {
		this.updateprofile = updateprofile;
	}
	
	
}
