package com.etel.dashboard.forms;

public class ResignationBucket {
	private Integer approvedresignation = 0;
	private Integer releasedmembership = 0;
	private Integer declinedresignation = 0;
	private Integer deferredresignation = 0;
	
	public Integer getApprovedresignation() {
		return approvedresignation;
	}
	public void setApprovedresignation(Integer approvedresignation) {
		this.approvedresignation = approvedresignation;
	}
	public Integer getReleasedmembership() {
		return releasedmembership;
	}
	public void setReleasedmembership(Integer releasedmembership) {
		this.releasedmembership = releasedmembership;
	}
	public Integer getDeclinedresignation() {
		return declinedresignation;
	}
	public void setDeclinedresignation(Integer declinedresignation) {
		this.declinedresignation = declinedresignation;
	}
	public Integer getDeferredresignation() {
		return deferredresignation;
	}
	public void setDeferredresignation(Integer deferredresignation) {
		this.deferredresignation = deferredresignation;
	}

}
