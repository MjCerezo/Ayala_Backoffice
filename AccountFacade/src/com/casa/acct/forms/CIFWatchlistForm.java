package com.casa.acct.forms;

public class CIFWatchlistForm {
	
	private String cifno;
	private String cifname;
	private Boolean amla;
	private Boolean blacklist;
	private Boolean dosri;
	private Boolean fatca;
	private Boolean pep;
	public String getCifno() {
		return cifno;
	}
	public void setCifno(String cifno) {
		this.cifno = cifno;
	}
	public String getCifname() {
		return cifname;
	}
	public void setCifname(String cifname) {
		this.cifname = cifname;
	}
	public Boolean getAmla() {
		return amla;
	}
	public void setAmla(Boolean amla) {
		this.amla = amla;
	}
	public Boolean getBlacklist() {
		return blacklist;
	}
	public void setBlacklist(Boolean blacklist) {
		this.blacklist = blacklist;
	}
	public Boolean getDosri() {
		return dosri;
	}
	public void setDosri(Boolean dosri) {
		this.dosri = dosri;
	}
	public Boolean getFatca() {
		return fatca;
	}
	public void setFatca(Boolean fatca) {
		this.fatca = fatca;
	}
	public Boolean getPep() {
		return pep;
	}
	public void setPep(Boolean pep) {
		this.pep = pep;
	}
	
	
}
